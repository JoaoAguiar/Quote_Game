const button1 = document.getElementById("option_one")
const button2 = document.getElementById("option_two")

let shuffledQuestions = []
let questionNumber    = 1
let playerScore       = 0  
let wrongAttempt      = 0 
let indexNumber       = 0

var music1 = new Audio('assets/gustavo1.mp3');
var music2 = new Audio('assets/jesus1.mp3');
var music3 = new Audio('assets/gustavo2.mp3');
var music4 = new Audio('assets/jesus2.mp3');

// Displaying next question in the array to DOM
function NextQuestion(index) {
    handleQuestions()

    const currentQuestion = shuffledQuestions[index]
    
    document.getElementById("display_question").innerHTML = currentQuestion.question;
    document.getElementById("option_one").innerHTML = currentQuestion.optionA;
    document.getElementById("option_two").innerHTML = currentQuestion.optionB;
}

// Shuffle and push 10 questions to shuffledQuestions array
function handleQuestions() { 
    while (shuffledQuestions.length <= 9) {
        const random = questions[Math.floor(Math.random() * questions.length)]
        
        if (!shuffledQuestions.includes(random)) {
            shuffledQuestions.push(random)
        }
    }
}

function handleNextQuestion(x) {
    button1.disabled = true
    button2.disabled = true

    checkAnswer(x)
    
    setTimeout(() => {
        if (indexNumber <= 9) {
            NextQuestion(indexNumber)

            button1.disabled = false
            button2.disabled = false
        }
        else {
            handleEndGame()
            
            button1.disabled = false
            button2.disabled = false
        }
    }, 4000);
}

function checkAnswer(x) {
    const currentQuestion = shuffledQuestions[indexNumber] // Get question 
    const currentAnswer = currentQuestion.correctOption    // Get answer
    
    let option = null
    
    if(x === 1) {
        option = document.getElementById("option_one");
    }
    else {
        option = document.getElementById("option_two");
    }
    
    if(option.value === currentAnswer) {
        playerScore++
        indexNumber++

        setTimeout(() => {
            notShow()
        }, 3950);

        show(0)

        if(option.value === "optionA") {
            music4.play();

            setTimeout(() => {
                music4.pause();
                music4.currentTime = 0
            }, 3950);
        }
        else {
            music3.play();

            setTimeout(() => {
                music3.pause();
                music4.currentTime = 0
            }, 3950);
        }
    }
    else if(option.value !== currentAnswer) { 
        wrongAttempt++
        indexNumber++

        setTimeout(() => {
            notShow()
        }, 3950);

        show(1)

        if(option.value === "optionA") {
            music2.play();

            setTimeout(() => {
                music2.pause();
                music4.currentTime = 0
            }, 3950);
        }
        else {
            music1.play();

            setTimeout(() => {
                music1.pause();
                music4.currentTime = 0
            }, 3950);
        }
    }
}

// Function for when all questions being answered
function handleEndGame() {
    const grade = (playerScore / 10) * 100

    // Data to display to score board
    document.getElementById('grade_percentage').innerHTML = grade
    document.getElementById('wrong_answers').innerHTML = wrongAttempt
    document.getElementById('right_answers').innerHTML = playerScore
    document.getElementById('score_modal').style.display = "flex"
}

// Closes score and resets
function closeScoreModal() {
    questionNumber    = 1
    playerScore       = 0
    wrongAttempt      = 0
    indexNumber       = 0
    shuffledQuestions = []

    NextQuestion(indexNumber)
    document.getElementById('score_modal').style.display = "none";
}

function notShow() {
    document.getElementById('noice').style.display = "none"
    document.getElementById('not_noice').style.display = "none"
}
function show(y) {
    if(y === 0) {
        document.getElementById('noice').style.display = "flex"
    }
    else {
        document.getElementById('not_noice').style.display = "flex"
    }
}